         private void limpiar() {

        txtHoraInicio.setEnabled(true);
        tbHorario.clearSelection();
        tbHorario.clearSelection();
        txtHoraInicio.setText("");
        txtHoraFIn.setText("");
        cargarTabla();
        horarioControl.setHorario(null);

    }


