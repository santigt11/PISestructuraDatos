    private void cargarVista() {
        int fila = tbHorario.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Escoja un registro de la tabla", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                horarioControl.setHorario(mtp.getHorarios().getInfo(fila));
                txtHoraInicio.setText(horarioControl.getHorario().getHoraInicio());
                txtHoraInicio.setText("");
                txtHoraInicio.setText(horarioControl.getHorario().getHoraFin());
                cbDisponible.setEnabled(true);
               boolean estaDisponible = horarioControl.getHorario().getDisponible();

// Establecer el estado del checkbox basado en la disponibilidad
             cbDisponible.setSelected(estaDisponible);

    }catch (Exception ex) {
                Logger.getLogger(FrmGuardarPersona.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }
      private void cargarTabla() {
        mtp.setHorarios(horarioControl.all());
//        horarioControl.setHorarios(horarioControl.all());
        tbHorario.setModel(mtp);
        tbHorario.updateUI();
    }
        public Boolean verificar() {
        return (!txtHoraInicio.getText().trim().isEmpty()
                && !txtHoraFIn.getText().trim().isEmpty());
    }
       
